import java.util.Locale;

public class Main {

    public static void main(String[] args) {
        // Primitivos vs Objetos

        String s = "texto"; // A variável "s" é um objeto, pois possui atributos e comportamentos
        s.toUpperCase(); // Este é um comportamento da variável "s"

        // Em contrapartida, tipos primitivos apenas carregam valores, contudo, para atribuir coisas e comportamentos á
        // um primitivo, existe as classes Wrapper
        int a = 123; // A variável "a" é um tipo primitivo
    }

}
