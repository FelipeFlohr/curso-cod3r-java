public class Main {

    public static void main(String[] args) {

        // Inferência de tipos
        // Pode-se usar inferência na declaração do tipo de uma variável,
        // veja:

        var a = 4.5; // Usando "var", o compilador irá automaticamente designar um tipo para a variável, que nesse caso
                     // é double. É necessário inicializá-la juntamente ao "var" também.
        System.out.println("Variável a: "+a);

    }

}
