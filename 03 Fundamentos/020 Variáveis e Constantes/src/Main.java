public class Main {

    public static void main(String[] args) {
        // Declarando uma variável ou constante
        // Variável -> Tipo + nome da variável = valor
        // Constante -> final + tipo + NOME DA VARIÁVEL = valor
        double raio = 3.4;
        final double PI = 3.14159;
        double area = PI * raio * raio;

        System.out.println(area);
    }

}
