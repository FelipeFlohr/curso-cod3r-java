public class Veiculo {

    final String cor;
    final int numRodas;
    final double preco;
    final String nome;

    Veiculo(String cor, int numRodas, double preco, String nome) {
        this.cor = cor;
        this.numRodas = numRodas;
        this.preco = preco;
        this.nome = nome;
    }

}
